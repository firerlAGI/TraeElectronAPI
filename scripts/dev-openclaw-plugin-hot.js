#!/usr/bin/env node
const fs = require("node:fs");
const path = require("node:path");
const PLUGIN_ID = "traeclaw";

function buildHotReloadLayout(options = {}) {
  const rootDir = path.resolve(options.rootDir || path.join(__dirname, ".."));
  const hotRootDir = path.resolve(options.hotRootDir || path.join(rootDir, ".runtime", "openclaw-plugin-hot"));
  return {
    rootDir,
    sourcePluginDir: path.join(rootDir, "integrations", "openclaw-trae-plugin"),
    hotRootDir,
    hotPluginDir: path.join(hotRootDir, PLUGIN_ID),
    generatedConfigPath: path.join(hotRootDir, "openclaw.dev.config.json"),
    readmePath: path.join(hotRootDir, "README.dev-hot.txt"),
    markerPath: path.join(hotRootDir, PLUGIN_ID, "HOT_PLUGIN_README.txt")
  };
}

function resolveQuickstartCommand(rootDir, platform = process.platform) {
  if (platform === "win32") {
    return `"${path.join(rootDir, "start-traeapi.cmd")}"`;
  }
  return `"${path.join(rootDir, "start-traeapi.command")}"`;
}

function buildOpenClawDevConfig(layout, options = {}) {
  const platform = options.platform || process.platform;
  return {
    plugins: {
      load: {
        paths: [layout.hotPluginDir]
      },
      entries: {
        [PLUGIN_ID]: {
          enabled: true,
          config: {
            baseUrl: "http://127.0.0.1:8787",
            autoStart: true,
            quickstartCommand: resolveQuickstartCommand(layout.rootDir, platform),
            quickstartCwd: layout.rootDir
          }
        }
      }
    },
    tools: {
      alsoAllow: [PLUGIN_ID]
    },
    agents: {
      list: [
        {
          id: "main",
          tools: {
            alsoAllow: ["trae_status", "trae_update_self", "trae_new_chat", "trae_open_project", "trae_switch_mode", "trae_delegate"]
          }
        }
      ]
    }
  };
}

function buildDevHotReadme(layout) {
  return [
    "OpenClaw dev hot plugin directory",
    "",
    "This directory is generated by scripts/dev-openclaw-plugin-hot.js.",
    "Do not edit files here directly.",
    "",
    `Source plugin dir: ${layout.sourcePluginDir}`,
    `Hot plugin dir: ${layout.hotPluginDir}`,
    `Generated OpenClaw config: ${layout.generatedConfigPath}`,
    "",
    "Important:",
    "- Point OpenClaw at the hot plugin dir, not the source plugin dir.",
    "- Keep TraeClaw quickstartCommand and quickstartCwd pointing at the development repository root.",
    "- If OpenClaw does not auto-reload changed plugin code, restart OpenClaw Gateway after sync."
  ].join("\n");
}

function buildHotPluginMarker(layout) {
  return [
    "Generated plugin mirror for OpenClaw development.",
    "Do not edit this directory directly.",
    `Source: ${layout.sourcePluginDir}`
  ].join("\n");
}

function ensureSourcePlugin(layout) {
  if (!fs.existsSync(layout.sourcePluginDir)) {
    throw new Error(`Source plugin directory was not found: ${layout.sourcePluginDir}`);
  }
}

function syncHotPluginDirectory(options = {}) {
  const layout = options.layout || buildHotReloadLayout(options);
  ensureSourcePlugin(layout);

  fs.mkdirSync(layout.hotRootDir, { recursive: true });
  fs.rmSync(layout.hotPluginDir, {
    recursive: true,
    force: true
  });
  fs.cpSync(layout.sourcePluginDir, layout.hotPluginDir, {
    recursive: true,
    force: true
  });
  fs.writeFileSync(layout.generatedConfigPath, `${JSON.stringify(buildOpenClawDevConfig(layout, options), null, 2)}\n`, "utf8");
  fs.writeFileSync(layout.readmePath, `${buildDevHotReadme(layout)}\n`, "utf8");
  fs.writeFileSync(layout.markerPath, `${buildHotPluginMarker(layout)}\n`, "utf8");

  return {
    rootDir: layout.rootDir,
    sourcePluginDir: layout.sourcePluginDir,
    hotRootDir: layout.hotRootDir,
    hotPluginDir: layout.hotPluginDir,
    generatedConfigPath: layout.generatedConfigPath,
    syncedAt: new Date().toISOString()
  };
}

function formatSummary(summary, reason = "manual") {
  return [
    `[TraeClaw] Hot plugin sync completed (${reason})`,
    `- Source plugin dir: ${summary.sourcePluginDir}`,
    `- Hot plugin dir: ${summary.hotPluginDir}`,
    `- OpenClaw config template: ${summary.generatedConfigPath}`,
    `- Synced at: ${summary.syncedAt}`,
    "- Point OpenClaw to the hot plugin dir above, not integrations/openclaw-trae-plugin."
  ].join("\n");
}

function watchHotPluginDirectory(layout) {
  let timer = null;

  const scheduleSync = (reason) => {
    clearTimeout(timer);
    timer = setTimeout(() => {
      try {
        const summary = syncHotPluginDirectory({ layout });
        console.log(formatSummary(summary, reason));
      } catch (error) {
        console.error(`[TraeClaw] Hot plugin sync failed: ${error.message}`);
      }
    }, 120);
  };

  const watcher = fs.watch(layout.sourcePluginDir, { recursive: true }, (eventType, filename) => {
    const label = filename ? `${eventType}:${filename}` : eventType;
    scheduleSync(label);
  });

  watcher.on("error", (error) => {
    console.error(`[TraeClaw] Hot plugin watcher error: ${error.message}`);
  });

  return watcher;
}

async function main(argv = process.argv.slice(2)) {
  const watchMode = argv.includes("--watch");
  const jsonMode = argv.includes("--json");
  const layout = buildHotReloadLayout();
  const summary = syncHotPluginDirectory({ layout });

  if (jsonMode) {
    process.stdout.write(`${JSON.stringify(summary, null, 2)}\n`);
  } else {
    console.log(formatSummary(summary));
  }

  if (!watchMode) {
    return;
  }

  console.log("[TraeClaw] Watching source plugin directory for changes...");
  watchHotPluginDirectory(layout);
  await new Promise(() => {});
}

if (require.main === module) {
  main().catch((error) => {
    console.error(`[TraeClaw] ${error.message}`);
    process.exit(1);
  });
}

module.exports = {
  buildHotReloadLayout,
  buildOpenClawDevConfig,
  buildDevHotReadme,
  syncHotPluginDirectory,
  formatSummary,
  resolveQuickstartCommand
};
